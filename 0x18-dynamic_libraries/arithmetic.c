#include "main.h"
/**
 * sum_add - a function that computes the
 * summation of two integers
 * @a: first integer
 * @b: second integer
 * return: sum
 */
int sum_add(int a, int b)
{
	return (a + b);
}

/**
 * modulus - a function that gets the remainder
 * between two divided integers
 * @a: first integer
 * @b: second integer
 * return: the remainder
 */
int modulus(int a, int b)
{
	return (a % b);
}

/**
 * subtract - a function that subtracts
 * two integers
 * @a: first integer
 * @b: second integer
 * return: the subtraction
 */
int subtract(int a, int b)
{
	return (a - b);
}

/**
 * divide - a function that divides
 * two integrs 
 * @a: first integer
 * @b: second integer
 * return: the division
 */
int divide(int a, int b)
{
	return (a / b);
}

/**
 * multiple - a function that multiples
 * two integrs
 * @a: first integer
 * @b: second integer
 * return: the multiple
 */
int multiple(int a, int b)
{
	return (a * b);
}
